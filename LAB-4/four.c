// area of triangle

#include<stdio.h>

int main()
{
        float area;
        int base = 10, height = 10;
        
        printf("Given base and height of triangle is: %d %d \n", base, height);
        
        area = (base * height);
        
        printf("Area of triangle is: %f\n", area);
        
        return 0;
}
